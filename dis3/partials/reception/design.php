<td id = "picTableHead">Creating Slides in Google Docs</td>
</tr>
<tr><td id = "picTableCell">
<p>
Before creating a slide, type up all text in a separate document, and save all necessary images.  Once you have all of the material for your slide, begin by choosing a format.  
There are layouts available for single and double column articles, news listings, schedules, etc.  Once you have chosen the layout you are going to use, open
the google doc.  Select "Make a copy" from the "File" menu.  In the copy, delete any text/images already on the layout.  You can paste text from another source by
making a text box (click the text box icon, then drag where you want the box to be) and then adding text.  To add images, go to the "Insert" menu, select "Image", and
choose from a file menu the image you want to add.  Once all of the material is added to your slide, select objects to resize/move them.  If you need to change the
slide template, go to "View" menu and select "Master".  Click on the highest slide labeled "Master" on the left side of the page.  This will take you to the master pages,
where you can edit the slide template. 
</p>
</td></tr></table>

<table id="picTable">
<tr>
<td id = "picTableHead">Saving and Uploading Slides</td>
</tr>
<tr><td id = "picTableCell">
<p>
Once you are content with the layout of your slides, you can save and upload them to the server, to be displayed on the department monitors.  To save the slide as an image,
select "File", "Download As", then "PNG image (.png)".  The png option results in the best image quality, and other formats should not be used.  This should cause an image
file to be downloaded onto your computer.  To upload it onto the server, visit the "Manage Images" page of this site and upload your file.  Make sure to specify its
position in the rotation, or else it will not display.  If you are adding your file to any position in rotation that is not last place, be sure to change the positions of 
other files accordingly.
</p>
</td></tr></table>
